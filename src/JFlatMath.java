
public class JFlatMath {
	public static float sqrti(int n) {
		return (float) Math.sqrt(n);
	}

	public static float sqrtr(float f) {
		return (float) Math.sqrt(f);
	}

	public static float toReal(int i) {
		return (float) i;
	}

	public static int toInt(float f) {
		return (int) f;
	}

	public static float random() {
		return (float) Math.random();
	}
}
